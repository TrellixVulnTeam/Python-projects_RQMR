num1 = float(input("enter first number : "))
opp = input("enter opertor: ")
num2 = float(input("enter secnd numbr: "))
if  opp == "+":
     print(num1 + num2)
elif opp == "-":
     print( num1 - num2)
elif opp == "*":
     print( num1 * num2 )
elif opp == "/":
     print(num1 / num2)
else:
     print("invalid")